"""Generate ``trials-data.js`` (window.TRIALS_DATA) from trials/*.json.

The GUI (app.js, owned by Codex) renders Trials + per-Trial boards from this blob.
Output is deterministic (sorted), so re-running without changes is byte-identical.

    python generate_trials_data.py
"""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent


def _load(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def _objective_record(root: Path, entry: dict) -> dict:
    task_path = root / entry["path"]
    title = entry["path"]
    summary = ""
    if task_path.exists():
        task = _load(task_path)
        title = task.get("title", task.get("id", entry["path"]))
        summary = task.get("description", "")
    return {
        "id": Path(entry["path"]).parent.name,
        "category": entry.get("category", ""),
        "difficulty": entry.get("difficulty", ""),
        "weight": int(entry.get("weight", 1)),
        "title": title,
        "summary": summary,
    }


def build_trials_data(root: Path = ROOT) -> dict:
    root = Path(root)
    trials_dir = root / "trials"
    trials = []
    if trials_dir.exists():
        for p in sorted(trials_dir.glob("*.json")):
            data = _load(p)
            objectives = [_objective_record(root, e) for e in data.get("objectives", [])]
            objectives.sort(key=lambda o: (o["category"], o["id"]))
            sections = {}
            for o in objectives:
                sections[o["category"]] = sections.get(o["category"], 0) + 1
            trials.append({
                "id": data.get("id", p.stem),
                "name": data.get("name", p.stem),
                "description": data.get("description", ""),
                "objectiveCount": len(objectives),
                "totalWeight": sum(o["weight"] for o in objectives),
                "sections": dict(sorted(sections.items())),
                "objectives": objectives,
            })
    trials.sort(key=lambda t: t["id"])
    return {"count": len(trials), "trials": trials}


def write_trials_data_js(root: Path, out_path: Path) -> dict:
    data = build_trials_data(root)
    blob = json.dumps(data, indent=2)
    Path(out_path).write_text(
        "// AUTO-GENERATED by generate_trials_data.py - do not edit by hand.\n"
        "// Regenerate: python generate_trials_data.py\n"
        f"window.TRIALS_DATA = {blob};\n",
        encoding="utf-8",
    )
    return data


def main() -> int:
    out = ROOT / "trials-data.js"
    data = write_trials_data_js(ROOT, out)
    print(f"Wrote {out}\n  trials={data['count']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
