"""Generate ``setups-data.js`` from the on-disk ``setups/`` corpus.

The GUI's Setups section renders from ``window.SETUPS_DATA``: each setup's manifest
plus the (read-only) contents of its instruction file and skills, so non-coders can
SEE the kit their AI runs with. Mirrors ``generate_atlas_data.py`` (deterministic,
sorted; ``window.X = {blob}``).

``usedInRuns`` counts leaderboard entries whose ``setup_id`` matches.

Run::

    python generate_setups_data.py
"""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent
MAX_FILE_CHARS = 20000  # cap embedded content so the data blob stays small


def _load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def _read_capped(path: Path) -> str:
    try:
        text = path.read_text(encoding="utf-8")
    except (OSError, UnicodeDecodeError):
        return ""
    if len(text) > MAX_FILE_CHARS:
        return text[:MAX_FILE_CHARS] + "\n... (truncated)\n"
    return text


def _kind(path: Path) -> str:
    return {".json": "json", ".md": "markdown"}.get(path.suffix.lower(), "text")


def _skill_purpose(text: str) -> str:
    """First frontmatter ``description:``, else first non-trivial body line."""
    lines = text.splitlines()
    for ln in lines:
        s = ln.strip()
        if s.lower().startswith("description:"):
            return s.split(":", 1)[1].strip()
    for ln in lines:
        s = ln.strip()
        if s and not s.startswith("---") and not s.startswith("name:"):
            return s
    return ""


def _setup_files(setup_dir: Path) -> list:
    out = []
    for p in sorted(setup_dir.rglob("*")):
        if p.is_dir() or p.name == ".gitkeep":
            continue
        out.append({
            "path": p.relative_to(setup_dir).as_posix(),
            "kind": _kind(p),
            "content": _read_capped(p),
        })
    return out


def _skills(setup_dir: Path, manifest: dict) -> list:
    out = []
    for name in (manifest.get("skills") or []):
        skill_md = setup_dir / "skills" / name / "SKILL.md"
        content = _read_capped(skill_md) if skill_md.exists() else ""
        out.append({
            "name": name,
            "purpose": _skill_purpose(content),
            "path": f"skills/{name}/SKILL.md",
            "content": content,
        })
    return out


def _used_in_runs(root: Path) -> dict:
    counts = {}
    entries_dir = root / "leaderboard" / "entries"
    if entries_dir.exists():
        for p in entries_dir.glob("*.json"):
            try:
                sid = _load_json(p).get("setup_id")
            except (OSError, json.JSONDecodeError):
                continue
            if sid:
                counts[sid] = counts.get(sid, 0) + 1
    return counts


def _badges(manifest: dict) -> list:
    if manifest.get("badges"):
        return list(manifest["badges"])
    if not manifest.get("instructions_file") and not (manifest.get("skills") or []):
        return ["baseline"]
    return []


def _setup_record(setup_dir: Path, used: dict) -> dict:
    manifest = _load_json(setup_dir / "setup.json")
    sid = manifest.get("id", setup_dir.name)
    instr_file = manifest.get("instructions_file")
    instr_content = ""
    if instr_file and (setup_dir / instr_file).exists():
        instr_content = _read_capped(setup_dir / instr_file)
    return {
        "id": sid,
        "name": manifest.get("name", sid),
        "description": manifest.get("description", ""),
        "agent": manifest.get("agent", ""),
        "model": manifest.get("model"),
        "badges": _badges(manifest),
        "usedInRuns": used.get(sid, 0),
        "contextPack": manifest.get("context_pack"),
        "instructions": {"file": instr_file, "content": instr_content},
        "skills": _skills(setup_dir, manifest),
        "files": _setup_files(setup_dir),
    }


def build_setups_data(root: Path = ROOT) -> dict:
    root = Path(root)
    setups_dir = root / "setups"
    used = _used_in_runs(root)
    records = []
    if setups_dir.exists():
        for manifest in sorted(setups_dir.glob("*/setup.json")):
            try:
                records.append(_setup_record(manifest.parent, used))
            except (OSError, json.JSONDecodeError):
                continue
    records.sort(key=lambda r: r["id"])
    return {"count": len(records), "setups": records}


def write_setups_data_js(root: Path, out_path: Path) -> dict:
    data = build_setups_data(root)
    blob = json.dumps(data, indent=2)
    Path(out_path).write_text(
        "// AUTO-GENERATED by generate_setups_data.py - do not edit by hand.\n"
        "// Regenerate: python generate_setups_data.py\n"
        f"window.SETUPS_DATA = {blob};\n",
        encoding="utf-8",
    )
    return data


def main() -> int:
    out = ROOT / "setups-data.js"
    data = write_setups_data_js(ROOT, out)
    print(f"Wrote {out}\n  setups={data['count']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
