"""Generate ``leaderboard-data.js`` from ``leaderboard/entries/*.json``.

The static leaderboard page (``leaderboard.html`` + ``leaderboard.js``) renders from
``window.LEADERBOARD_DATA``. Entries are submitted eval-summaries (see LEADERBOARD.md).
Ranking: weighted pass-rate desc (primary); ties -> fewer total tokens, then less time;
entries with any unsafe change are sorted below all clean entries and flagged.
Output is deterministic (sorted), so re-running without entry changes is byte-identical.

Run::

    python generate_leaderboard_data.py
"""
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent


def _entry_row(e: dict) -> dict:
    ti = e.get("tokens_in")
    to = e.get("tokens_out")
    total = e.get("tokens_total")
    if total is None and (ti is not None or to is not None):
        total = (ti or 0) + (to or 0)
    agg = e.get("aggregate_stats") or {}
    return {
        "agent_label": e.get("agent_label", e.get("set_id", "unknown")),
        "model": e.get("model", ""),
        "set_id": e.get("set_id", ""),
        "weighted_pass_rate": agg.get("weighted_pass_rate", 0.0),
        "unsafe": (e.get("status_counts") or {}).get("unsafe", 0),
        "tokens_in": ti,
        "tokens_out": to,
        "tokens_total": total,
        "wall_clock_seconds": e.get("wall_clock_seconds"),
        "cost_usd": e.get("cost_usd"),
        "metrics_self_reported": e.get("metrics_self_reported", True),
    }


_INF = float("inf")


def _sort_key(r: dict):
    return (
        1 if r["unsafe"] else 0,                                   # clean before flagged
        -float(r["weighted_pass_rate"] or 0.0),                   # higher pass-rate first
        r["tokens_total"] if r["tokens_total"] is not None else _INF,
        r["wall_clock_seconds"] if r["wall_clock_seconds"] is not None else _INF,
        r["agent_label"],                                         # stable final tiebreak
    )


def build_leaderboard_data(root: Path = ROOT) -> dict:
    root = Path(root)
    entries_dir = root / "leaderboard" / "entries"
    rows = []
    if entries_dir.exists():
        for p in sorted(entries_dir.glob("*.json")):
            try:
                rows.append(_entry_row(json.loads(p.read_text(encoding="utf-8"))))
            except (OSError, json.JSONDecodeError):
                continue
    rows.sort(key=_sort_key)
    for i, r in enumerate(rows, 1):
        r["rank"] = i
    return {"count": len(rows), "entries": rows}


def write_leaderboard_data_js(root: Path, out_path: Path) -> dict:
    data = build_leaderboard_data(root)
    blob = json.dumps(data, indent=2)
    Path(out_path).write_text(
        "// AUTO-GENERATED by generate_leaderboard_data.py - do not edit by hand.\n"
        "// Regenerate: python generate_leaderboard_data.py\n"
        f"window.LEADERBOARD_DATA = {blob};\n",
        encoding="utf-8",
    )
    return data


def main() -> int:
    out = ROOT / "leaderboard-data.js"
    data = write_leaderboard_data_js(ROOT, out)
    print(f"Wrote {out}\n  entries={data['count']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
